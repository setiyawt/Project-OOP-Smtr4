    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int hasil = 0;
        conBar = new ControllerBarang();

        String nama_kategori = jTextField5.getText();
        String nama_barang = jTextField2.getText();
        int harga_barang = Integer.parseInt(jTextField3.getText());
        int stok_barang = Integer.parseInt(jTextField4.getText());

        if ((jTextField5.equals("")) || (jTextField2.equals(""))
                || (jTextField3.equals(""))
                || (jTextField4.equals(""))) {
            JOptionPane.showMessageDialog(null, "Fill all the required fields!!");
        } else {
            BarangModel bm = new BarangModel(nama_kategori, nama_barang, harga_barang, stok_barang);
            hasil = conBar.saveBrg(bm);
            switch (hasil) {
                case 1:
                    JOptionPane.showMessageDialog(this, "The item has been successfully added.");
                    loadBarang();
                    clearData();
                    break;
                case -1:
                    JOptionPane.showMessageDialog(this, "The type has already been registered.");
                    jTextField1.requestFocusInWindow();
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Failed to save the data.");
                    break;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

