    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String nama_barang;
        int id_barang, hasil = 0;
        id_barang = Integer.parseInt(jTextField1.getText());
        nama_barang = jTextField2.getText();

        int quest = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the data?" + nama_barang + "?", "Info",
                JOptionPane.YES_NO_OPTION);

        if (quest == JOptionPane.YES_OPTION) {
            BarangModel bm = new BarangModel();
            bm.setId_barang(id_barang);

            conJenis = new ControllerBarang();
            hasil = conJenis.deleteBrg(bm);

            if (hasil == 1) {
                JOptionPane.showMessageDialog(this, "The data has been successfully deleted!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the data.");
            }

            loadBarang();
            clearData();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

