    private void tabelBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelBarangMouseClicked
        int baris = tabelBarang.rowAtPoint(evt.getPoint()); //mengambil nilai dari kolom indeks 0 (kolom pertama) di baris yang diklik, kemudian mengonversinya menjadi string dan menyimpannya dalam variabel id.
        int id_barang = Integer.parseInt(tabelBarang.getValueAt(baris, 0).toString());
        jTextField1.setText(String.valueOf(id_barang)); //mengatur nilai id ke dalam jTextField1 untuk menampilkan nilainya kepada pengguna
        String nama_kategori = tabelBarang.getValueAt(baris, 1).toString(); //mengambil nilai dari kolom indeks 1 (kolom kedua) di baris yang diklik, kemudian mengonversinya menjadi string dan menyimpannya dalam variabel nama.
        jTextField5.setText(nama_kategori); //mengatur nilai nim ke dalam jTextField2 untuk menampilkan nilainya kepada pengguna.
        String nama_barang = tabelBarang.getValueAt(baris, 2).toString(); //mengambil nilai dari kolom indeks 2 (kolom ketiga) di baris yang diklik, kemudian mengonversinya menjadi string dan menyimpannya dalam variabel nim.
        jTextField2.setText(nama_barang); //mengatur nilai nama ke dalam jTextField3 untuk menampilkan nilainya kepada pengguna
        int harga_barang = Integer.parseInt(tabelBarang.getValueAt(baris, 3).toString());
        jTextField3.setText(String.valueOf(harga_barang));
        int stok_barang = Integer.parseInt(tabelBarang.getValueAt(baris, 4).toString());
        jTextField4.setText(String.valueOf(stok_barang));

    }//GEN-LAST:event_tabelBarangMouseClicked

