    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int hasil = 0;
        conCart = new ControllerCart();
        if (jTextField7.equals("") || jTextField5.equals("") || jTextField6.equals("") || jTextField4.equals("")
                || jTextField5.equals("") || jTextField6.equals("")) {
            JOptionPane.showMessageDialog(this, "Please select the item first!");
        } else if (jTextField1.equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill in the quantity field first!");
        } else {
            CartModel cm = new CartModel(mLogin.getId_user(),
                    Integer.parseInt(jTextField7.getText()),
                    jTextField5.getText(),
                    jTextField6.getText(),
                    Integer.parseInt(jTextField4.getText()),
                    Integer.parseInt(jTextField1.getText()),
                    Integer.parseInt(jTextField2.getText()));
            hasil = conCart.saveCart(cm);
            if (hasil == 1) {
                JOptionPane.showMessageDialog(this, "Successfully added to the cart.",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add item to the cart.", "Failed!",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        clearData();
        loadCart();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        conTrans = new ControllerTransaksi();
        conBar = new ControllerBarang();
        conCart = new ControllerCart();

        Object[][] obCart = new Object[jTable1.getRowCount()][jTable1.getColumnCount()];

        int i = 0;
        try {
            for (i = 0; i < jTable1.getRowCount(); i++) {
                BarangModel bm = new BarangModel();
                obCart[i][0] = jTable1.getValueAt(i, 0);// id barang
                obCart[i][1] = jTable1.getValueAt(i, 1);// nama kategori
                obCart[i][2] = jTable1.getValueAt(i, 2);// nama barang
                obCart[i][3] = jTable1.getValueAt(i, 3);// harga barang
                obCart[i][4] = jTable1.getValueAt(i, 4);// jumlah barang
                obCart[i][5] = jTable1.getValueAt(i, 5);// sub total
                TransaksiModel tm = new TransaksiModel(Integer.parseInt(
                        obCart[i][0].toString()), mLogin.getId_user(),
                        Integer.parseInt(obCart[i][4].toString()),
                        Integer.parseInt(obCart[i][5].toString()));
                conTrans.saveTrx(tm);
                
                
            }
            conCart.deleteCart(mLogin);
            loadCart();
            loadBarang();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to add data.", "Failed",
                    JOptionPane.ERROR_MESSAGE);
        }
        JOptionPane.showMessageDialog(this, "Thankyou for your purchase");
        


    }//GEN-LAST:event_jButton3ActionPerformed

