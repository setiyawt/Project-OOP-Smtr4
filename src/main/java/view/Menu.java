    private void loadCart() {
        int total = 0;
        List<CartModel> lsCart = new ArrayList<CartModel>();
        conCart = new Controller.ControllerCart();
        lsCart = conCart.getCart(mLogin);

        Object[][] obCart = new Object[lsCart.size()][7];

        int i = 0;

        for (CartModel cm : lsCart) {
            obCart[i][0] = cm.getId_barang();
            obCart[i][1] = cm.getNama_kategori();
            obCart[i][2] = cm.getNama_barang();
            obCart[i][3] = cm.getHarga_barang();
            obCart[i][4] = cm.getJumlah_barang();
            obCart[i][5] = cm.getTotal();
            total += cm.getTotal();
            i++;
        }
        jTextField3.setText(String.valueOf(total));
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                obCart,
                new String[]{
                    "Product ID", "Categories", "Product", "Price", "Qty", "Sub Total"
                }));
        tabelBarang.setDefaultEditor(Object.class, null);
    }

    private void loadBarang() {
        List<BarangModel> lsBarang = new ArrayList<BarangModel>();
        ControllerBarang conBar = new ControllerBarang();
        lsBarang = conBar.getBrg();

        Object[][] obBar = new Object[lsBarang.size()][6];

        int i = 0;

        for (BarangModel bm : lsBarang) {
            obBar[i][0] = bm.getId_barang();
            obBar[i][1] = bm.getNama_kategori();
            obBar[i][2] = bm.getNama_barang();
            obBar[i][3] = bm.getHarga_barang();
            obBar[i][4] = bm.getStok_barang();
            i++;
        }
        tabelBarang.setModel(new javax.swing.table.DefaultTableModel(
                obBar,
                new String[]{
                    "Product ID", "Categories", "Product", "Price", "Stock"
                }));
        tabelBarang.setDefaultEditor(Object.class, null);
    }

