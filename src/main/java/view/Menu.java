    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int hasil = 0;
        int quest = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the cart data?", "Delete Cart",
                JOptionPane.YES_NO_OPTION);
        if (quest == JOptionPane.YES_OPTION) {
            conCart = new ControllerCart();
            hasil = conCart.deleteCart(mLogin);

            if (hasil == 1) {
                JOptionPane.showMessageDialog(this, "The cart has been successfully deleted.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the data.");
            }
            loadCart();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

